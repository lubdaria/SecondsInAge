let secondsInAMinute = 60;
let minuteInAnHour = 60;

let secondsInAnHour = secondsInAMinute * minuteInAnHour;
console.log(secondsInAnHour);

let hourInADay = 24;

let secondInADay = secondsInAnHour * hourInADay;
console.log(secondInADay);

let dayInAYear = 365;

let secondsInAYear = secondInADay * dayInAYear;
console.log(secondsInAYear);

let age = 21;
 
let secondsInAge = secondsInAYear * age;
console.log(secondsInAge);